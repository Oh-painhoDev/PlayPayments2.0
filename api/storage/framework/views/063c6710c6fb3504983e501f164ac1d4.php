function switchTab(tab) {
    // Hide all tab panels
    document.querySelectorAll('[role="tabpanel"]').forEach(panel => {
        panel.setAttribute('hidden', '');
        panel.setAttribute('data-state', 'inactive');
    });
    
    // Remove active state from all tabs
    document.querySelectorAll('[role="tab"]').forEach(tabBtn => {
        tabBtn.setAttribute('aria-selected', 'false');
        tabBtn.setAttribute('data-state', 'inactive');
    });
    
    // Show selected tab panel
    const tabPanel = document.getElementById('tab-' + tab);
    if (tabPanel) {
        tabPanel.removeAttribute('hidden');
        tabPanel.setAttribute('data-state', 'active');
    }
    
    // Set active state on selected tab
    const tabTrigger = document.getElementById('tab-' + tab + '-trigger');
    if (tabTrigger) {
        tabTrigger.setAttribute('aria-selected', 'true');
        tabTrigger.setAttribute('data-state', 'active');
    }
}

