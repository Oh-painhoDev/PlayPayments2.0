// Constantes para cálculo de crypto
const CRYPTO_MIN_AMOUNT = 5000.00;
const CRYPTO_FIXED_FEE = 25.00;
const CRYPTO_PERCENTAGE_FEE = 0.05; // 5%
const USDT_QUOTE = 5.68; // Cotação mockada (pode ser substituída por API real)

function updateUsdtQuote() {
    // Atualizar cotação USDT (mock - pode ser substituído por API real)
    document.getElementById('usdtQuote').textContent = `R$ ${USDT_QUOTE.toFixed(2)}`;
    
    // Configurar eventos para cálculo em tempo real
    const amountInput = document.getElementById('cryptoWithdrawalAmount');
    const usdtInput = document.getElementById('cryptoWithdrawalAmountUsdt');
    const feeDisplay = document.getElementById('cryptoFeeDisplay');
    const feeAmount = document.getElementById('cryptoFeeAmount');
    const feeTotal = document.getElementById('cryptoFeeTotal');
    const totalToPay = document.getElementById('cryptoTotalToPay');
    const receiveAmount = document.getElementById('cryptoReceiveAmount');
    const minAmountWarning = document.getElementById('cryptoMinAmountWarning');
    const submitBtn = document.getElementById('cryptoWithdrawalSubmitBtn');
    const availableBalance = <?php echo e($availableBalance ?? 0); ?>;
    
    // Função para calcular e atualizar valores
    function calculateCryptoWithdrawal() {
        const amount = parseFloat(amountInput.value) || 0;
        
        // Validar valor mínimo
        if (amount > 0 && amount < CRYPTO_MIN_AMOUNT) {
            minAmountWarning.classList.remove('hidden');
            minAmountWarning.classList.add('text-red-500');
            submitBtn.disabled = true;
            submitBtn.classList.add('opacity-50', 'cursor-not-allowed');
        } else {
            minAmountWarning.classList.add('hidden');
        }
        
        // Validar saldo disponível
        if (amount > availableBalance) {
            minAmountWarning.classList.remove('hidden');
            minAmountWarning.textContent = `Saldo insuficiente. Disponível: R$ ${availableBalance.toFixed(2).replace('.', ',')}`;
            minAmountWarning.classList.add('text-red-500');
            submitBtn.disabled = true;
            submitBtn.classList.add('opacity-50', 'cursor-not-allowed');
        } else if (amount >= CRYPTO_MIN_AMOUNT && amount <= availableBalance) {
            minAmountWarning.classList.add('hidden');
            submitBtn.disabled = false;
            submitBtn.classList.remove('opacity-50', 'cursor-not-allowed');
        }
        
        if (amount >= CRYPTO_MIN_AMOUNT && amount <= availableBalance) {
            // Calcular taxa: R$ 25,00 + 5.00%
            const percentageFeeAmount = amount * CRYPTO_PERCENTAGE_FEE;
            const totalFeeAmount = CRYPTO_FIXED_FEE + percentageFeeAmount;
            
            // Valor líquido após taxa
            const netAmount = amount - totalFeeAmount;
            
            // Calcular USDT recebido
            const usdtAmount = netAmount / USDT_QUOTE;
            
            // Atualizar displays
            feeAmount.textContent = `Taxa calculada: R$ ${totalFeeAmount.toFixed(2).replace('.', ',')}`;
            feeTotal.textContent = `R$ ${totalFeeAmount.toFixed(2).replace('.', ',')}`;
            totalToPay.textContent = `R$ ${amount.toFixed(2).replace('.', ',')}`;
            receiveAmount.textContent = `${usdtAmount.toFixed(4)} USDT`;
            usdtInput.value = usdtAmount.toFixed(4);
        } else {
            // Resetar valores se inválido
            feeAmount.textContent = 'Taxa calculada: R$ 0,00';
            feeTotal.textContent = 'R$ 0,00';
            totalToPay.textContent = 'R$ 0,00';
            receiveAmount.textContent = '0.0000 USDT';
            usdtInput.value = '0.0000';
        }
    }
    
    // Remover event listeners anteriores para evitar duplicação
    amountInput.removeEventListener('input', calculateCryptoWithdrawal);
    amountInput.removeEventListener('change', calculateCryptoWithdrawal);
    amountInput.removeEventListener('blur', calculateCryptoWithdrawal);
    
    // Adicionar event listeners
    amountInput.addEventListener('input', calculateCryptoWithdrawal);
    amountInput.addEventListener('change', calculateCryptoWithdrawal);
    amountInput.addEventListener('blur', calculateCryptoWithdrawal);
    
    // Calcular inicialmente se houver valor
    if (amountInput.value) {
        calculateCryptoWithdrawal();
    }
}

